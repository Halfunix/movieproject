package pkg.article;

public class ArticleVO {
	private String articleID, articleAuthor, articleContent, articleRegdate, articleStar, articleMovieId;
	
	public String getArticleID() {
		return articleID;
	}
	
	public void setArticleID(String articleID) {
		this.articleID = articleID;
	}
	
	public String getArticleAuthor() {
		return articleAuthor;
	}
	
	public void setArticleAuthor(String articleAuthor) {
		this.articleAuthor = articleAuthor;
	}
	
	public String getArticleContent() {
		return articleContent;
	}
	
	public void setArticleContent(String articleContent) {
		this.articleContent = articleContent;
	}
	
	public String getArticleRegdate() {
		return articleRegdate;
	}
	
	public void setArticleRegdate(String articleRegdate) {
		this.articleRegdate = articleRegdate;
	}
	
	public String getArticleStar() {
		return articleStar;
	}
	
	public void setArticleStar(String articleStar) {
		this.articleStar = articleStar;
	}
	
	public String getArticleMovieId() {
		return articleMovieId;
	}
	
	public void setArticleMovieId(String articleMovieId) {
		this.articleMovieId = articleMovieId;
	}
}
